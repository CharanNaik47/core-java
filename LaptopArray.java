class LaptopArray {
    private String model;
    private int ram;

    public void setModel(String model) {
        this.model = model;
    }

    public String getModel() {
        return this.model;
    }

    public void setRam(int ram) {
        this.ram = ram;
    }

    public int getRam() {
        return this.ram;
    }
}
